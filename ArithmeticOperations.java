package firstpackage;

public class ArithmeticOperations {
	static void add()
	 {
		int a=20;
		int b=30;
		int c=a+b;
		System.out.println(c);
	 }
	
	 static void subtract()
	 {
		 int a=30;
		 int b=20;
		 int c=a-b;
		 System.out.println(c);
	 }
	 
	 static void multiply()
	 {
		 int a=30;
		 int b=20;
		 int c=a*b;
		 System.out.println(c);
	 }
	 static void divide()
	 {
		 int a=40;
		 int b=20;
		 int c=a/b;
		 System.out.println(c);
	 }
	 static void modulus()
	 {
		 int a=30;
		 int b=20;
		 int c=a%b;
		 System.out.println(c);
	 }

	public static void main(String[] args) {
		add();
		subtract();
		multiply();
		divide();
		modulus();		

	}

}
